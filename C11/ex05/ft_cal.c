int	ft_plus(int x, int y)
{
	return (x + y);
}

int	ft_minus(int x, int y)
{
	return (x - y);
}

int	ft_mul(int x, int y)
{
	return (x * y);
}

int	ft_div(int x, int y)
{
	return (x / y);
}

int	ft_rem(int x, int y)
{
	return (x % y);
}
